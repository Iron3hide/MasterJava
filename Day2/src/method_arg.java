class method_arg{

    static void sayHelloworldtwice(){
        System.out.println("HelloWorld");
        System.out.println("HelloWorld");
        System.out.println();
    }

    static void sayHelloworldthrice(){
        System.out.println("HelloWorld");
        System.out.println("HelloWorld");
        System.out.println("HelloWorld");
    }

    public static void main(String[] args){
        sayHelloworldtwice();
        sayHelloworldthrice();
    }
}